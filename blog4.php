						<h2 style="margin-top: 40px;font-weight: bold;">The Uncanny Relationship Between Content Creators and Brands</h2>
                        <p>Not a long time ago, Content Creators were termed as Influencers. What does the word influence tell us? 
                            From my perspective, it projects the idea of, to shape the mind of the consumer to behave in a certain way, 
                            by sharing your thoughts which are now perceived as important by them.
                            <br>
                            But slowly that word started taking a wrong turn. This happened because a lot of “Influencers” started 
                            shopping on it by sharing wrong information, bad brands, non-ethical products, etc.

                        </p>
                        
                        <p>The real OG’s took matters into their own hands. They made sure to shed the right light and lead by example. 
                            Those bright lamp posts on creators' street always maintained the strong stand of being called a content creator 
                            at any given event and not a “Influencer”. This was not something we call “Groupie” things, where they came 
                            together and decided on such a stand. The inner conscience of each creator with ethical standards started 
                            feeling the same way, and resonated with the same vibe, leading to appreciating one another in this journey.

                        </p>  

                        
                        <p>This took a big shift of being an Ethical Content Creator in the Digital Space. 
                            We all know, no one wants to be associated with negative backlashes. 
                            So, slowly but steadily many of the creators started taking their “perception sharing” seriously. This big shift 
                            even made consumer convenience about their lovable creators doing something right. <br>
                            Leading to re-building of the community with stronger bonds than ever.
                            Many noticed this shift, especially the Brands
                            </p>

                        <p>Brands started to replan their entire marketing strategies over ethical sharing with the creator’s community. 
